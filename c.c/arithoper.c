#include<stdio.h>
void main()
{
    int a,b;
    printf("Enter value of a:");
    scanf("%d",&a);
    printf("\n Enter value of b:");
    scanf("%d",&b);
    {
        printf("\n a+b=%d",(a+b));
        printf("\n a-b=%d",(a-b));
        printf("\n a*b=%d",(a*b));
        printf("\n a/b=%d",(a/b));
        printf("\n a%b=%d",(a%b));
}
}